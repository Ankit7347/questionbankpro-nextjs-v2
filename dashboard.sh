create_page() {
  DIR_PATH="$DASHBOARD_ROOT/$1" 
  FILE_PATH="$DIR_PATH/page.tsx" if [ -f 
  "$FILE_PATH" ]; then
    echo "SKIP: $FILE_PATH already exists" 
    return 0
  fi mkdir -p "$DIR_PATH" echo "/**" > 
  "$FILE_PATH" echo " * Route File: 
  $FILE_PATH" >> "$FILE_PATH" echo " * 
  Auto-generated by dashboard route script" 
  >> "$FILE_PATH"
  echo " */" >> "$FILE_PATH" echo "export 
  default function Page() {" >> "$FILE_PATH" 
  echo " return null;" >> "$FILE_PATH" echo 
  "}" >> "$FILE_PATH" echo "CREATED: 
  $FILE_PATH"
}
